    private void Logar() {
        
        try {
          
            String matricula_funcionario;
            String senha_funcionario;
        
            matricula_funcionario = txtMatricula.getText();
            senha_funcionario = txtSenha.getText();
        
            FuncionarioDTO funcionarioDto = new FuncionarioDTO();
        
            funcionarioDto.setMatricula(matricula_funcionario);
            funcionarioDto.setSenha(senha_funcionario);
            
            FuncionarioValidacaoLogin funcionarioDao = new FuncionarioValidacaoLogin();
            
            ResultSet rsfuncionarioDao = funcionarioDao.autenticacaoFuncionario(funcionarioDto);
            
            if (rsfuncionarioDao.next() ) {
                //Chamar tela que eu quero abrir
                formPrincipalView  formprincipalview = new formPrincipalView();
                formprincipalview.setVisible(true);
                
                dispose();
                
            } else {
                //Enviar Mensagem dizendo incorreto
                JOptionPane.showMessageDialog(null, "Matricula ou senha Inválida");
            }
                
          
      
      } catch (SQLException erro) {
          
          JOptionPane.showMessageDialog(null, "FORMLOGINVIEW" + erro);
      }
    }

